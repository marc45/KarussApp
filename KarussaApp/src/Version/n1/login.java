    private void iniciziar(int canal) {
        if (canal > 0) {
            Principal principal = new Principal(canal);
            principal.setVisible(true);
            principal.setVisible(false);
            principal.toBack();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña mal, intentelo de nuevo");
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
    }

    private void ExisteArchivo() {
        se = new serializador();
        String datos[] = se.leerArchivo();
        if (datos[0] != null && datos[1] != null) {
            this.usuario = datos[0];
            this.password = datos[1];
            int canal = Integer.parseInt(obtenerID(this.usuario, this.password));
            iniciziar(canal);
        }
    }
