    public String obtenerID(String user, String pass) {
        try {
            String url = "http://" + conf.DameUrl() + conf.Direccion();
            PeticionesWeb pp = new PeticionesWeb(url);
            String algo = pp.sendPOST(user, pass);
            try {
                if (Integer.parseInt(algo) > 0) {
                    se = new serializador();
                    se.serializar(user, pass);
                }
            } catch (Exception e) {
                System.out.println("Fallo serializacion");
                System.out.println(e);
            }
            return algo;
        } catch (Exception e) {
            System.out.println("fallo peticion web");
            System.out.println(e);
            return "-1";
        }
    }

